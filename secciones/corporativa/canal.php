<p class="Arial_14_regular_marron">La Nueva Televisión del Sur (teleSUR), es una empresa multiestatal latinoamericana inició sus actividades el 24 de julio de 2005, teniendo como lema "Nuestro Norte es el Sur". Fue impulsada en un principio por el presidente de la República Bolivariana de Venezuela, Hugo Chávez, junto con el apoyo de Fidel Castro los gobiernos de Cuba, Argentina y Uruguay, con los cuales se trazan y efectúan los planes para su desarrollo. Posteriormente se sumaron como aliados Bolivia en el 2006, y un año después Nicaragua y Ecuador.</p>
<p class="Arial_14_regular_marron"><strong>teleSUR</strong> ofrece a través de su señal 24 horas de continua transmisión e información en español, funcionando como una ventana comunicacional desde Nuestra América para el resto del mundo. El canal difunde mediante su programación la lucha por las transformaciones sociales, por la democratización de la comunicación y por la integración de la región, desde la pluralidad y diversidad. Con una información contextualizada y balanceada, nuestra meta es ayudar a formar ciudadanos críticos, informados y participativos en nuestras sociedades.</p>

<p class="Arial_14_regular_marron">Este proyecto de integración latinoamericana, busca desarrollar una nueva estrategia comunicacional que promueva el derecho a la información, asumiendo la veracidad como principio y estimular la producción, promoción y difusión de contenidos propios de la región fomentando así el reconocimiento del imaginario latinoamericano.</p>

<p class="Arial_14_regular_marron">El canal cuenta con 12 corresponsalías permanentes en Bogotá, Brasilia, Buenos Aires, Caracas, Ciudad de México, La Habana, La Paz, Lima, Managua, Puerto Príncipe, Quito y Washington, además de contar con una extensa red de colaboradores en toda la región latinoamericana y otros lugares del mundo.</p>

<p class="Arial_14_regular_marron">La señal del canal es recibida en los hogares de millones de personas en todo el mundo, a través de satélites, cable operadores, internet y televisoras abiertas que cubren América del Sur, Centroamérica, El Caribe, Estados Unidos, Europa Occidental, el Norte de África y parte del Medio Oriente, gracias a la suscripción de acuerdos y convenios comunicacionales con otras televisoras y cadenas de noticias, tanto latinoamericanas como fuera de nuestro continente.</p>

